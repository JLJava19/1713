/**
 * Ejercicios resueltos con operadores ++ y -- (operadores incremento y decremento)
Ejercicio 1: Si x es una variable de tipo int con valor 10, determina qué se muestra 
por pantalla cuando se ejecutan las siguientes instrucciones:
int x = 10;
System.out.println(x);
x++;
System.out.println(x);
System.out.println(++x);
System.out.println(x++);
System.out.println(x);
System.out.println(x++);
System.out.println(++x);
System.out.println(++x);
++x;
x++;
System.out.println(++x);
System.out.println(x++);
System.out.println(++x);
 */

package com.ipartek.formacion.ejercicios.basicos;

/**
 * 
 * @author Curso
 *
 */

public class Ejercicio5 {

	public static void main(String[] args) {
		
		int x = 10;
		System.out.println(x);
		x++;
		System.out.println(x);
		System.out.println(++x);
		System.out.println(x++);
		System.out.println(x);
		System.out.println(x++);
		System.out.println(++x);
		System.out.println(++x);
		++x;
		x++;
		System.out.println(++x);
		System.out.println(x++);
		System.out.println(++x);
	}

}
